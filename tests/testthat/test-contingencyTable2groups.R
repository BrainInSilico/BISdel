test_that('test contingency', {
  x <- c(0, 1, 0, 1, 0, 1, 0, 0)
  y <- c(0, 1, 0, 1, 0, 1, 1, 1)
  cTable <- contingencyTable2groups(x, y)
  expect_equal(unname(cTable[1,1]), 5)
  expect_equal(unname(cTable[1,2]), 3)
  expect_equal(unname(cTable[2,1]), 3)
  expect_equal(unname(cTable[2,2]), 5)

  x <- c(rep(0,19), rep(1,30))
  y <- c(rep(0,12), rep(1,39))
  cTable <- contingencyTable2groups(x, y)
  expect_equal(unname(cTable[1,1]), 19)
  expect_equal(unname(cTable[1,2]), 30)
  expect_equal(unname(cTable[2,1]), 12)
  expect_equal(unname(cTable[2,2]), 39)

  x <- c(rep(0,19), rep(1,30), rep(-1,8))
  y <- c(rep(0,12), rep(1,39), rep(2,34))
  cTable <- contingencyTable2groups(x, y)
  expect_equal(unname(cTable[1,1]), 8)
  expect_equal(unname(cTable[1,2]), 19)
  expect_equal(unname(cTable[1,3]), 30)
  expect_equal(unname(cTable[1,4]), 0)
  expect_equal(unname(cTable[2,1]), 0)
  expect_equal(unname(cTable[2,2]), 12)
  expect_equal(unname(cTable[2,3]), 39)
  expect_equal(unname(cTable[2,4]), 34)
})
